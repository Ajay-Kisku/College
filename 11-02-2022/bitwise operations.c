#include <stdio.h>

int main(){

// &, |, ^, ~, >> and <<

    //Binary Left shift
    printf("\nBinary Left shift 1<<1 : %d\n", 1<<1);    //2
    printf("\nBinary Left shift 2<<1 : %d\n", 2<<1);    //4

    // Binary Right shift
    printf("\nBinary Right shift 2>>1 : %d\n", 2>>1);   //1
    printf("\nBinary Right shift 1>>1 : %d\n", 1>>1);   //0

    return 0;
}